/**
 * RideConnect Material Design 3 Theme
 * Requirements: 22.1, 22.2, 22.3, 22.4, 22.5, 22.6
 * 
 * @param theme Theme preference (LIGHT, DARK, or SYSTEM_DEFAULT)
 * @param dynamicColor Whether to use dynamic colors on Android 12+
 * @param content Composable content
 */
@Composable
fun RideConnectTheme(
    theme: Theme = Theme.SYSTEM_DEFAULT,
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    val systemInDarkTheme = isSystemInDarkTheme()
    
    // Determine if dark theme should be used
    // Requirements: 22.4, 22.5
    val darkTheme = when (theme) {
        Theme.LIGHT -> false
        Theme.DARK -> true
        Theme.SYSTEM_DEFAULT -> systemInDarkTheme
    }
    
    // Select color scheme
    // Requirements: 22.1
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }
    
    // Update system bars
    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.primary.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}
